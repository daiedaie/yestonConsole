public class ProductInfoVo {

	private String productName;
	private String productId;
	private String productCode;
	private String fixedYield;
	private String fixedDays;
	private String raiseMoney;
	private String reachMoney;
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductId() {
		return productId;
	}
	public void setProductId(String productId) {
		this.productId = productId;
	}
	public String getProductCode() {
		return productCode;
	}
	public void setProductCode(String productCode) {
		this.productCode = productCode;
	}
	public String getFixedYield() {
		return fixedYield;
	}
	public void setFixedYield(String fixedYield) {
		this.fixedYield = fixedYield;
	}
	public String getFixedDays() {
		return fixedDays;
	}
	public void setFixedDays(String fixedDays) {
		this.fixedDays = fixedDays;
	}
	public String getRaiseMoney() {
		return raiseMoney;
	}
	public void setRaiseMoney(String raiseMoney) {
		this.raiseMoney = raiseMoney;
	}
	public String getReachMoney() {
		return reachMoney;
	}
	public void setReachMoney(String reachMoney) {
		this.reachMoney = reachMoney;
	}
	
}
